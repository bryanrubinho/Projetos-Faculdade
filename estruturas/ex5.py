#5 Elaborar um algoritmo que receba os valores A, B e C, e mostre na tela se a soma de A + B é menor que C
a=float(input("Digite um valor para representar A:\n"))
b=float(input("Digite um valor para representar B:\n"))
c=float(input("Digite um valor para representar C:\n"))
if(a+b<c):
    print("a+b é menor que c")
else:
    print("a+b não é menor que c")